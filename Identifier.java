
public class Identifier {
	public static void main(String[] args) {
		int MyVar=1, myvar=2;
		int $sum=10, _sum=20;
		int Package=30, 컴공과=40;
		//문법 오류 발생 int 1Myvar = 10;
		System.out.println("MyVar = " + MyVar + ", myvar = " + myvar);
		System.out.println("$sum = " + $sum + ", _sum =" + _sum);
		System.out.println("Package = " + Package + ", 컴공과 = "+ 컴공과);
		
	}
}
